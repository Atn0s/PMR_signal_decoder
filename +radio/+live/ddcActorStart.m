function actor = ddcActorStart(pool, ddcSpec, varargin)
%DDCACTORSTART Start one ordered, persistent matrix-DDC worker.
p = inputParser;
p.addParameter('MaxQueueSec', 2.0);
p.parse(varargin{:});
if isempty(pool) || isa(pool, 'parallel.ThreadPool')
    error('radio:live:ddcActorStart:Pool', ...
        'The live DDC actor requires a process pool.');
end
persistent nextActorId
if isempty(nextActorId), nextActorId = uint64(1); end
actorId = nextActorId;
nextActorId = nextActorId + uint64(1);
outputQueue = parallel.pool.PollableDataQueue;
future = parfeval(pool, @radio.live.ddcActorWorker, 0, ...
    ddcSpec, outputQueue, actorId);
actor = struct( ...
    'actorId', actorId, ...
    'future', future, ...
    'outputQueue', outputQueue, ...
    'inputQueue', [], ...
    'ready', false, ...
    'resetPending', false, ...
    'stopped', false, ...
    'failed', false, ...
    'flushed', false, ...
    'pendingMessages', {cell(0, 1)}, ...
    'pendingInputSamples', uint64(0), ...
    'processedInputSamples', uint64(0), ...
    'ringAttached', false, ...
    'ringDrained', false, ...
    'ringReadSequence', uint64(0), ...
    'ringWriteSequence', uint64(0), ...
    'ringSourceSampleEnd', uint64(0), ...
    'ringConsumedSampleEnd', uint64(0), ...
    'maxQueueSec', double(p.Results.MaxQueueSec), ...
    'computeCount', uint64(0), ...
    'totalComputeSec', 0, ...
    'maxComputeSec', 0, ...
    'errorReason', '');
end
