function colorCode = getColorCode(bits)
%GETCOLORCODE Decode dPMR 24-bit channel color code.
if numel(bits) ~= 24
    error('dpmr:getColorCode:BadLength', 'dPMR channel code must be 24 bits.');
end
channelCode = bitor(uint32(dpmr.bitsToInt(bits)), uint32(hex2dec('555555')));
keys = uint32([ ...
hex2dec('575F77'), hex2dec('577577'), hex2dec('57DD75'), hex2dec('57F775'), ...
hex2dec('55577D'), hex2dec('557D7D'), hex2dec('55D57F'), hex2dec('55FF7F'), ...
hex2dec('5F555F'), hex2dec('5F7F5F'), hex2dec('5FD75D'), hex2dec('5FFD5D'), ...
hex2dec('5D5D55'), hex2dec('5D7755'), hex2dec('5DDF57'), hex2dec('5DF557'), ...
hex2dec('775DD7'), hex2dec('7777D7'), hex2dec('77DFD5'), hex2dec('77F5D5'), ...
hex2dec('7555DD'), hex2dec('757FDD'), hex2dec('75D7DF'), hex2dec('75FDDF'), ...
hex2dec('7F57FF'), hex2dec('7F7DFF'), hex2dec('7FD5FD'), hex2dec('7FFFFD'), ...
hex2dec('7D5FF5'), hex2dec('7D75F5'), hex2dec('7DDDF7'), hex2dec('7DF7F7'), ...
hex2dec('D755F7'), hex2dec('D77FF7'), hex2dec('D7D7F5'), hex2dec('D7FDF5'), ...
hex2dec('D55DFD'), hex2dec('D577FD'), hex2dec('D5DFFF'), hex2dec('D5F5FF'), ...
hex2dec('DF5FDF'), hex2dec('DF75DF'), hex2dec('DFDDDD'), hex2dec('DFF7DD'), ...
hex2dec('DD57D5'), hex2dec('DD7DD5'), hex2dec('DDD5D7'), hex2dec('DDFFD7'), ...
hex2dec('F75757'), hex2dec('F77D57'), hex2dec('F7D555'), hex2dec('F7FF55'), ...
hex2dec('F55F5D'), hex2dec('F5755D'), hex2dec('F5DD5F'), hex2dec('F5F75F'), ...
hex2dec('FF5D7F'), hex2dec('FF777F'), hex2dec('FFDF7D'), hex2dec('FFF57D'), ...
hex2dec('FD5575'), hex2dec('FD7F75'), hex2dec('FDD777'), hex2dec('FDFD77')]);
idx = find(keys == channelCode, 1, 'first');
if isempty(idx)
    colorCode = -1;
else
    colorCode = idx - 1;
end
end

